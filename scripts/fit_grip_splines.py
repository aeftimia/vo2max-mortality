# ── Celis-Morales 2018 HR constants ──────────────────────────────────────────
# HR per 5 kg LOWER grip strength (HR > 1 = weaker is worse), sex-stratified
HR_PER_5KG_LOWER = {'male': 1.16, 'female': 1.20}
HR_PER_5KG_LOWER_CI = {'male': (1.15, 1.17), 'female': (1.17, 1.23)}

# Convert to protective-direction base: hr_raw = base^(grip/5)
def _base(hr_5kg):
    return 1.0 / hr_5kg

GRIP_FLOOR = 0.0

# ── Pipeline config ──────────────────────────────────────────────────────────

